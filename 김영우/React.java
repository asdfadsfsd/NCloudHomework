package review.weeks03;

public class React {

	public int width;
	public int height;
	
	public void React() {
		
	}
	
	public void React(int width, int height) {
		this.width = width;
		this.height = height;
	}
	
	public double Round() {
		return width * 4;
	}
	
	public double Area () {
		return width * height;
	}
	
}
