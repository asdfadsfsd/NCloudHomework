package homework3;

public class BasicSmatPhone {
    public int number;
    public int user;
    public int network;
    
    public BasicSmatPhone()
    {
    
    }

	public int getNumber() {
		return number;
	}

	public void setNumber(int number) {
		this.number = number;
	}

	public int getUser() {
		return user;
	}

	public void setUser(int user) {
		this.user = user;
	}

	public int getNetwork() {
		return network;
	}

	public void setNetwork(int network) {
		this.network = network;
	}

	
    
}
